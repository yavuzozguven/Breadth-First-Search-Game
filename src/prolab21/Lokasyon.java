package prolab21;


public class Lokasyon {
    private int x;
    private int y;
    public Lokasyon(int x,int y){
        this.x = x;
        this.y = y;
    }
    public Lokasyon(){
        
    }

    
    public int getX() {
        return x;
    }

    
    public void setX(int x) {
        this.x = x;
    }

    
    public int getY() {
        return y;
    }

    
    public void setY(int y) {
        this.y = y;
    }
}
